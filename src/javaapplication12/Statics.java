    //Methode to remove a student from the system 
    private void removeStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeStudentActionPerformed
        String studentID = studentSearch.getText();
        if (studentID.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter the Student's ID");
        }else{
            int confirm = JOptionPane.showConfirmDialog(null,
            "Are you sure you want to delete this student?", "Confirm Delete",
            JOptionPane.YES_NO_OPTION);

             if (confirm != JOptionPane.YES_OPTION) {
             return; 
            }

            
            try(Connection conn = DriverManager.getConnection("jdbc:sqlite:db.db")) {
                String sql = "DELETE FROM Student WHERE ID = ? ";
                PreparedStatement pstmt = conn.prepareStatement(sql);
                pstmt.setString(1, studentID);
                
            int rowsAffected = pstmt.executeUpdate();
                if (rowsAffected>0) {
                      JOptionPane.showMessageDialog(null, "Student removed successfully");
            
                    idField.setText("");
                    nameField.setText("");
                    dateOfBirthField.setText("");
                    nationalIdField.setText("");
                    addressField.setText("");
                    emailField.setText("");
                    phoneField.setText("");
                    gradeField.setText("");
                    
                }else{
                JOptionPane.showMessageDialog(null, "Couldn't Remove ");
                }
                
            } catch (Exception e) {
            }
        
        
        
        }
    }//GEN-LAST:event_removeStudentActionPerformed
    
