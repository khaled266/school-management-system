    //button action to get the student data by his id 
    private void searchStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchStudentButtonActionPerformed
        studentInfo s = new studentInfo();
        
        
        String studentID = studentSearch.getText();
        s.setId(Integer.parseInt(studentSearch.getText()));
        
        if (studentID.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter the Student's ID");
        }else{
        
        try(Connection conn = DriverManager.getConnection("jdbc:sqlite:db.db")) {
            
            String sql = "SELECT * FROM Student WHERE ID = ? ";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, studentID);
            
            ResultSet rs = pstmt.executeQuery();
            if (rs.next()) {
                idField.setText(rs.getString("ID"));
                nameField.setText(rs.getString("Name"));
                dateOfBirthField.setText(rs.getString("DateOfBirth"));
                emailField.setText(rs.getString("Email"));
                nationalIdField.setText(rs.getString("NationalID"));
                addressField.setText(rs.getString("Address"));
                phoneField.setText(rs.getString("Phone"));
                gradeField.setText(rs.getString("Grade"));
                
            }else{
                JOptionPane.showMessageDialog(null, "No Student Found with this ID ");
                studentSearch.setText("");
            }
            
            
            
        } catch (Exception e) {
        }
        }
    }//GEN-LAST:event_searchStudentButtonActionPerformed
